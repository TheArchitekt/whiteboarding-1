# Write a function that takes in a list of numbers and prints out each number that is less than 10.
print('Less Than 10')

def less_than_10(list_num):
    for num in list_num:
        if num < 10:
            print(num)
list_of_numbers = [1, 40, 4, 7, 73, 12, 80, 110]
less_than_10(list_of_numbers)
